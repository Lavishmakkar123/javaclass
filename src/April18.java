import java.util.Scanner;

public class April18 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int i = 0;
        for (i = 1; i >= n; i++) {
            if (n == i) ;
            System.out.println("Prime number");
            if (i % n == 0) ;
            System.out.println("Prime number");
        }
    }
}
